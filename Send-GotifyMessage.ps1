Function Send-GotifyMessage{
[CmdletBinding()]
	Param (
		[Parameter(Mandatory = $True, Position = 0)]
        [string]$GotifyServer = $(Read-Host "Enter url"),
        [Parameter(Mandatory = $True, Position = 1)]
        [string]$Apptoken = $(Read-Host "Enter AppToken"),
        [Parameter(Mandatory = $True, Position = 2)]
        [string]$Title = $(Read-Host "Enter Title"),
        [Parameter(Mandatory = $True, Position = 3)]
        [string]$Message = $(Read-Host "Enter message"),
        [Parameter(Mandatory = $True, Position = 4)]
        [ValidateSet ("Min", "Low", "Normal", "High")]
        $Priority
        )
#Hash Table setting Urgency keys to an integer value
$global:Urgency =@{

       
        “Min” = “1”

        “Low” = “3”

        "Normal" = "6"

        "High" = "7"

        }

If ($Priority -eq "Min") {$Global:Msgurgency = $Urgency.min}
If ($Priority -eq "Low") {$Global:Msgurgency = $Urgency.Low}
If ($Priority -eq "Normal") {$Global:Msgurgency = $Urgency.Normal}
If ($Priority -eq "High") {$Global:Msgurgency = $Urgency.High}
#$Global:Msgurgency
#$msgtsend = $GotifyServer + "/message?token=" + $Apptoken + " " + "-F" + " " +"Title=$Title" + " " + "-F" + " " + "message=$Message"
$global:msg = $ExecutionContext.InvokeCommand.ExpandString('"$GotifyServer' + "/message?token=" + '$Apptoken"'+ " " + "-F" + " " + '"title=$Title"' + " " + "-F" + " " +'"message=$Message"' + " " + "-F" + " "+'"priority=$Global:Msgurgency"')

cmd.exe /c "curl.exe  $global:msg"
}
